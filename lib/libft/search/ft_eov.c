#include "../libft.h"

int	ft_eov(char *str)
{
	int	i;

	if (!str || ft_isdigit(str[0]))
		return (-1);
	i = 0;
	while (str[i] && (str[i] == '_' || ft_isalnum(str[i])))
		i++;
	return (i);
}